class CUB(Dataset):
    def __init__(self, root, is_train=True, transform=None, target_transform=None, loader=default_loader):
        self.data_dir = root
        self.transform = transform
        self.target_transform = target_transform
        self.loader = loader

        img_txt = os.path.join(self.data_dir, 'images.txt')
        image_class_labels_txt = os.path.join(self.data_dir, 'image_class_labels.txt')
        train_test_split = os.path.join(self.data_dir, 'train_test_split.txt')

        with open(img_txt, 'r', encoding='UTF-8') as f:
            img_list = [item.split()[1] for item in f.read().splitlines()]
        with open(image_class_labels_txt, 'r', encoding='UTF-8') as f:
            img_class_label_list = [int(item.split()[1]) - 1 for item in f.read().splitlines()]
        with open(train_test_split, 'r', encoding='UTF-8') as f:
            train_test_list = f.read().splitlines()

        idx_train_list = []
        idx_test_list = []
        for item in train_test_list:
            idx, flag = item.split()
            if int(flag) == 1:
                idx_train_list.append(int(idx) - 1)
            else:
                idx_test_list.append(int(idx) - 1)

        if is_train:
            self.imgs_path = [img_list[i] for i in idx_train_list]
            self.labels = [img_class_label_list[i] for i in idx_train_list]
        else:
            self.imgs_path = [img_list[i] for i in idx_test_list]
            self.labels = [img_class_label_list[i] for i in idx_test_list]

        print('===>load CUB datasets {} {} images...'.format(self.__len__(), 'train' if is_train else 'test'))

    def __len__(self):
        return len(self.labels)

    def __getitem__(self, index):
        img_name = self.imgs_path[index]
        img_path = os.path.join(self.data_dir, 'images', img_name)
        img = self.loader(img_path)
        label = self.labels[index]
        if self.transform:
            img = self.transform(img)
        if self.target_transform:
            label = self.target_transform(label)
        return img, label


